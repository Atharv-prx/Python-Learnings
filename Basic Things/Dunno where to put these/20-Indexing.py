# Indexing = accessing elements of a sequence using [] (indexing operator) 
# [Start : end : step]

credit_num = "1234-5678-9012-3456"

print(credit_num[0])
print(credit_num[0:4])
print(credit_num[-1])
print(credit_num[::2])  #Prints every 2nd character of string

print(credit_num[::-1])   #Prints reverse string